# Считываем целое число
number = int(input("Введите двухзначное число: "))

# Проверяем, что число действительно двухзначное
if 10 <= number <= 99:
    # Вычисляем правую и левую цифры
    right_digit = number % 10
    left_digit = number // 10

    # Выводим результат
    print("Правая цифра:", right_digit)
    print("Левая цифра:", left_digit)
else:
    print("Ошибка: Введите двухзначное число.")