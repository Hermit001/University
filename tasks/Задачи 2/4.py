# Считываем целое число
number = int(input("Введите целое число больше 9: "))

# Проверяем, что число больше 9
if number > 9:
    # Вычисляем цифру в разряде десятков
    tens_digit = (number // 10) % 10
    print("Цифра в разряде десятков числа:", tens_digit)
else:
    print("Ошибка: Введите число больше 9.")