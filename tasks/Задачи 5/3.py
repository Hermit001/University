# Считываем начальную дистанцию X и общую цель Y с ввода пользователя
X = float(input("Введите начальную дистанцию X (в километрах): "))
Y = float(input("Введите общую цель Y (в километрах): "))

# Инициализируем переменные
days = 1  # Начинаем с первого дня
current_distance = X  # Начальная дистанция

# Пока текущая дистанция меньше общей цели, увеличиваем дистанцию и дни
while current_distance < Y:
    current_distance += current_distance * 0.10  # Увеличиваем дистанцию на 10%
    days += 1

# Выводим количество дней, которое понадобится бегуну
print("Количество дней, которое понадобится бегуну:", days)