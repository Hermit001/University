# Считываем целое число с ввода пользователя
number = int(input("Введите целое число (не меньше 2): "))

# Проверяем, что число не меньше 2
if number >= 2:
    # Начинаем проверку делителей, начиная с 2
    divisor = 2
    
    # Итеративно увеличиваем делитель, пока не найдем наименьший
    while divisor <= number:
        if number % divisor == 0:
            # Найден делитель, который делит число без остатка
            print(divisor)
            break
        divisor += 1
else:
    print("Ошибка: Введенное число меньше 2.")