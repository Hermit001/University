# Инициализируем переменные
count = 0  # Счетчик элементов больше предыдущего
previous = None  # Предыдущий элемент, изначально неизвестен

# Вводим числа до тех пор, пока не введем 0
while True:
    num = int(input("Введите неотрицательное целое число (0 для завершения): "))
    
    # Проверяем, если введено 0, то завершаем ввод
    if num == 0:
        break
    
    # Проверяем, если предыдущий элемент есть и текущий элемент больше предыдущего
    if previous is not None and num > previous:
        count += 1
    
    # Устанавливаем текущий элемент как предыдущий для следующей итерации
    previous = num

# Выводим количество элементов больше предыдущего
print("Количество элементов, которые больше предыдущего:", count)
