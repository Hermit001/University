# Считываем значения основания и высоты с разных строк
base = int(input("Введите значение основания: "))
height = int(input("Введите значение высоты: "))

# Вычисляем площадь прямоугольного треугольника
area = 0.5 * base * height

# Выводим результат
print("Площадь прямоугольного треугольника:", area)