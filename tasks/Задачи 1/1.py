# Считываем три целых числа с разных строк
num1 = int(input("Введите первое целое число: "))
num2 = int(input("Введите второе целое число: "))
num3 = int(input("Введите третье целое число: "))

# Вычисляем сумму трех чисел
sum = num1 + num2 + num3

# Выводим результат
print("Сумма трех чисел:", sum)
