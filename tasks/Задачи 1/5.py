# Считываем количество студентов и количество яблок
N = int(input("Введите количество студентов: "))
K = int(input("Введите количество яблок: "))

# Вычисляем количество яблок у каждого студента
apples_per_student = K // N

# Вычисляем количество яблок в корзине
apples_in_basket = K % N

# Выводим результат
print(f"Яблок у студентов: {apples_per_student}, яблок в корзине: {apples_in_basket}")
