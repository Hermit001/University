# Считываем две строки чисел и разбиваем их на списки целых чисел
first_line = input("Введите первую строку чисел, разделенных пробелами: ").split()
second_line = input("Введите вторую строку чисел, разделенных пробелами: ").split()

# Преобразуем списки строк в списки целых чисел
first_nums = [int(num) for num in first_line]
second_nums = [int(num) for num in second_line]

# Инициализируем счетчик чисел из первой строки, которые встречаются во второй строке
count = 0

# Перебираем числа из первой строки
for num in first_nums:
    # Если число из первой строки есть во второй строке, увеличиваем счетчик
    if num in second_nums:
        count += 1

# Печатаем количество чисел из первой строки, которые встречаются во второй строке
print(count)
