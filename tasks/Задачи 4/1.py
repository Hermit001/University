# Функция для считывания целого положительного числа с проверкой
def read_positive_integer(prompt):
    while True:
        try:
            value = int(input(prompt))  # Считываем ввод пользователя как строку и преобразуем в целое число
            if value > 0:
                return value  # Возвращаем значение, если ввод корректен и положительный
            else:
                print("Ошибка: Введите положительное целое число.")
        except ValueError:
            print("Ошибка: Введите целое число.")

# Считываем целое положительное число
number = read_positive_integer("Введите целое положительное число: ")

# Проверяем, является ли число четным или нечетным
if number % 2 == 0:
    print("чет")
else:
    print("нечет")