# Считываем два целых числа с ввода пользователя
num1 = int(input("Введите первое целое число: "))
num2 = int(input("Введите второе целое число: "))

# Сравниваем числа и находим минимальное из них
min_number = min(num1, num2)

# Выводим минимальное число
print("Меньшее из чисел:", min_number)