# Считываем две целых координаты с ввода пользователя
vertical = int(input("Введите вертикальную координату (от 1 до 8): "))
horizontal = int(input("Введите горизонтальную координату (от 1 до 8): "))

# Проверяем, является ли клетка белой или черной
if (vertical + horizontal) % 2 == 0:
    print("Черная")
else:
    print("Белая")