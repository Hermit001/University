# Считываем целое положительное число из трех цифр с ввода пользователя
number = int(input("Введите целое положительное число из трех цифр: "))

# Получаем отдельные цифры из числа
digit1 = number // 100  # Первая цифра (сотни)
digit2 = (number // 10) % 10  # Вторая цифра (десятки)
digit3 = number % 10  # Третья цифра (единицы)

# Проверяем, идут ли цифры в восходящем порядке слева направо
if digit1 < digit2 < digit3:
    print("Да")
else:
    print("Нет")