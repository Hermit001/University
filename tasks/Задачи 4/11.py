# Считываем четыре целых координаты с ввода пользователя
vertical1 = int(input("Введите вертикальную координату первой клетки (от 1 до 8): "))
horizontal1 = int(input("Введите горизонтальную координату первой клетки (от 1 до 8): "))
vertical2 = int(input("Введите вертикальную координату второй клетки (от 1 до 8): "))
horizontal2 = int(input("Введите горизонтальную координату второй клетки (от 1 до 8): "))

# Проверяем, имеют ли клетки одинаковый цвет
if (vertical1 + horizontal1) % 2 == (vertical2 + horizontal2) % 2:
    print("Да")
else:
    print("Нет")
