# Считываем номер месяца с ввода пользователя
month = int(input("Введите номер месяца (от 1 до 12): "))

# Создаем список с числом дней в каждом месяце (для невисокосного года)
days_in_month = [31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31]

# Проверяем, что номер месяца в диапазоне от 1 до 12
if 1 <= month <= 12:
    # Получаем число дней в указанном месяце
    num_days = days_in_month[month - 1]
    print(f"Число дней в месяце {month}: {num_days}")
else:
    print("Ошибка: Введите номер месяца от 1 до 12.")